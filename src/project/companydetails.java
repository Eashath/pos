private void showdepth() {
        ArrayList<companyclass> list=companyclasslist();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setColumnIdentifiers(new Object[]{"companyname","companyphonenumber","companyemail","companyaddres",});
        Object [] row=new Object[4];
        
        for(int i=0;i<list.size();i++)
        {
            row[0]=list.get(i).getcompanyname();
            row[1]=list.get(i).getcompanyphonenumber();
            row[2]=list.get(i).getcompanyemail();
            row[3]=list.get(i).getcompanyaddres();

            
            
            model.addRow(row);
        }
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
    }

