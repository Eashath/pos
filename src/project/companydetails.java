    public ArrayList<companyclass>companyclasslist()
    {
    ArrayList<companyclass> companyclasslist=new ArrayList<>();
    try
    {
        conn=dbconnection.getConnection();
        PST=conn.prepareStatement("Select * from company");
        st=conn.createStatement();
        RS=st.executeQuery("Select * from company");
        companyclass companyclass;
        while(RS.next())
        {
        
            companyclass=new companyclass(RS.getString("companyname"),RS.getString("companyphonenumber"),RS.getString("companyemail"),RS.getString("companyaddres"));
            companyclasslist.add(companyclass);
        }
    
    }
    catch(SQLException e)
    {
        //JOptionPane.showMessageDialog(null, e);
    }
    return companyclasslist;
    }
    
